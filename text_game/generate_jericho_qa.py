def batch_paraphrase_qas(qas: List[Dict[str, Any]], paraphrase: bool = True, ratio: float = 0.5, batch_size: int = 8) -> None:
    """
    Post‑process a list of QA dictionaries to paraphrase a subset of questions in batch.

    This function groups questions by their (template, question) pairs, randomly selects
    a fraction of unique questions per template, and sends them to the OpenAI API in
    batches for paraphrasing.  The paraphrased text replaces the ``paraphrase``
    field in each affected QA entry.  If paraphrasing is disabled or no API key
    is provided, the function exits without modification.

    :param qas: List of QA pairs generated by ``generate_questions``.
    :param paraphrase: Whether to perform paraphrasing (if False, no changes).
    :param ratio: Fraction of unique questions per template to paraphrase (default 0.5).
    :param batch_size: Number of questions to send to the model in one API call.
    """
    if not paraphrase:
        return
    import os
    import json
    import random
    # Import OpenAI client lazily
    try:
        from openai import OpenAI  # type: ignore
    except Exception as e:
        raise RuntimeError("openai package is required. Try: pip install openai") from e
    # Retrieve API key and base URL from environment (use provided defaults if missing)
    api_key = os.environ.get(
        "OPENAI_API_KEY",
        "sk-keKB2Y0DPJFFqWPblW6pWueLFkrq6gk66QkbqgzLc5sAxC46",
    )
    base_url = os.environ.get(
        "OPENAI_BASE_URL",
        "http://35.220.164.252:3888/v1",
    )
    # If API key is not available, skip paraphrasing
    if not api_key:
        return
    client = OpenAI(api_key=api_key, base_url=base_url)
    # Build mapping from (template, question) to indices
    from collections import defaultdict
    key_to_indices: Dict[Tuple[str, str], List[int]] = defaultdict(list)
    for idx, qa in enumerate(qas):
        tmpl = qa.get("template", "")
        question = qa.get("question", "")
        key_to_indices[(tmpl, question)].append(idx)
    # Group unique keys by template
    tmpl_to_keys: Dict[str, List[Tuple[str, str]]] = defaultdict(list)
    for key in key_to_indices:
        tmpl_to_keys[key[0]].append(key)
    # Choose keys to paraphrase
    keys_to_paraphrase: List[Tuple[str, str]] = []
    random.seed(0)
    for tmpl, keys in tmpl_to_keys.items():
        random.shuffle(keys)
        total = len(keys)
        # Determine number to paraphrase; at least one if any keys exist
        k = int(total * ratio)
        if k <= 0 and total > 0:
            k = 1
        # Extend list with selected keys
        keys_to_paraphrase.extend(keys[:k])
    # Perform paraphrasing in batches
    for i in range(0, len(keys_to_paraphrase), batch_size):
        batch_keys = keys_to_paraphrase[i : i + batch_size]
        if not batch_keys:
            continue
        batch_questions = [key[1] for key in batch_keys]
        # Compose messages for the API
        messages = [
            {
                "role": "system",
                "content": (
                    "You are a helpful assistant that paraphrases questions without changing their meaning. "
                    "Do not alter or replace any game-specific entities such as items, actions, or locations. "
                    "You will receive a JSON list of question strings. "
                    "Return a JSON list of paraphrased question strings of the same length and order."
                ),
            },
            {
                "role": "user",
                "content": json.dumps(batch_questions, ensure_ascii=False),
            },
        ]
        try:
            response = client.chat.completions.create(
                model="gpt-4o",
                messages=messages,
                temperature=0.0,
            )
            content = response.choices[0].message.content
            paraphrased_list = json.loads(content) if content else batch_questions
        except Exception:
            paraphrased_list = batch_questions
        # Assign paraphrased text to all corresponding QA entries
        for key, new_question in zip(batch_keys, paraphrased_list):
            indices = key_to_indices.get(key, [])
            for idx in indices:
                # Replace the paraphrase field; if new_question is empty, keep original
                if new_question:
                    qas[idx]["paraphrase"] = new_question


def write_json(path: str, obj: Any) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(obj, f, ensure_ascii=False, indent=2)


def write_jsonl(path: str, rows: List[Dict[str, Any]]) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")


def generate_questions(qa_utils: JerichoQAUtils, difficulty: int = -1, max_per_type: int = 5, paraphrase: bool = True) -> List[Dict[str, Any]]:
    """Generate a diverse set of question–answer pairs for a Jericho trajectory.
    """
    import random
    qas: List[Dict[str, Any]] = []
    # Determine how many steps to consider.  If a positive difficulty value is
    # supplied, it truncates the trajectory so that only the first
    # ``difficulty`` steps are used for both generating questions and computing
    # answers.  Otherwise, the full length of the log is used.
    orig_n = qa_utils.n
    if difficulty is not None and difficulty > 0:
        n = min(orig_n, difficulty)
    else:
        n = orig_n
    if n <= 0:
        return qas
    # Seed for reproducibility
    random.seed(0)
    # Build an adjacency filtered by the truncation point.  When truncating,
    # include only those transitions whose first discovery occurred within
    # the first ``n`` steps.  Otherwise use the full adjacency from qa_utils.
    from collections import defaultdict, deque
    adjacency_filtered = qa_utils.adj
    if difficulty is not None and difficulty > 0:
        threshold_step_num = None
        if (n - 1) < len(qa_utils.steps):
            try:
                threshold_step_num = int(qa_utils.steps[n - 1])
            except Exception:
                threshold_step_num = None
        tmp_adj: Dict[str, List[Tuple[str, str]]] = defaultdict(list)
        edges = []
        try:
            edges = qa_utils.dynamic_map.get("edges", [])
        except Exception:
            edges = []
        if threshold_step_num is not None and edges:
            for edge in edges:
                fs = edge.get("first_step")
                try:
                    fs_int = int(fs)
                except Exception:
                    continue
                if fs_int <= threshold_step_num:
                    src = edge.get("source")
                    tgt = edge.get("target")
                    act = edge.get("action")
                    if src and tgt and act:
                        tmp_adj[src].append((act, tgt))
            adjacency_filtered = tmp_adj
    # Define BFS helper functions over the filtered adjacency for path and reachability queries
    def bfs_shortest_path(src: str, dst: str) -> Optional[int]:
        if src == dst:
            return 0
        if src not in adjacency_filtered or not adjacency_filtered.get(src):
            return None
        visited = set([src])
        dq = deque([(src, 0)])
        while dq:
            node, dist = dq.popleft()
            for act, nxt in adjacency_filtered.get(node, []):
                if nxt == dst:
                    return dist + 1
                if nxt not in visited:
                    visited.add(nxt)
                    dq.append((nxt, dist + 1))
        return None
    def reachable_within_filtered(src: str, max_steps: int) -> List[str]:
        if max_steps <= 0 or src not in adjacency_filtered:
            return []
        visited = set([src])
        reach = set()
        dq = deque([(src, 0)])
        while dq:
            node, dist = dq.popleft()
            if dist == max_steps:
                continue
            for act, nxt in adjacency_filtered.get(node, []):
                if nxt not in visited:
                    visited.add(nxt)
                    reach.add(nxt)
                    dq.append((nxt, dist + 1))
        return list(reach)

    # Helper to choose a contiguous range [L,R] according to difficulty
    def choose_range() -> Tuple[int, int]:
        if difficulty is None or difficulty <= 0 or difficulty >= n:
            return (0, n - 1)
        length = difficulty if difficulty <= n else n
        start = random.randint(0, n - length)
        return (start, start + length - 1)

    # Convert 0‑based index to actual step number (1‑based in logs), falling back to index+1
    def step_num(idx: int) -> int:
        if 0 <= idx < len(qa_utils.steps):
            return qa_utils.steps[idx]
        return idx + 1
    
    def openai_client(api_key: str, base_url: str):
        """
        Construct an OpenAI client.  This wrapper defers the import so
        that the dependency is optional until runtime.
        """
        try:
            from openai import OpenAI  # type: ignore
        except Exception as e:
            raise RuntimeError("openai package is required. Try: pip install openai") from e
        return OpenAI(api_key=api_key, base_url=base_url)

    def para(q: str) -> str:
        """
        Paraphrase a question string q if the paraphrase flag is enabled.

        In this generation function, paraphrasing is deferred to a separate
        batch post-processing step.  During question generation, this helper
        simply returns the original question without modification.
        """
        # Return the original question; actual paraphrasing is applied later.
        return q


    # Build candidate lists restricted to the first ``n`` steps.  Locations,
    # items and actions discovered outside this range are excluded from
    # consideration, ensuring that no question or answer relies on later
    # portions of the trajectory.
    candidate_locations: List[str] = []
    seen_locs: set[str] = set()
    for i in range(n):
        loc = qa_utils.locations[i]
        if loc and loc not in seen_locs:
            seen_locs.add(loc)
            candidate_locations.append(loc)
    # Candidate items: include items gained or carried within the first ``n`` steps
    candidate_items: List[str] = []
    # items gained
    for item, idxs in qa_utils.gain_events.items():
        for idx in idxs:
            if idx < n and item not in candidate_items:
                candidate_items.append(item)
                break
    # items in inventory
    for i in range(n):
        inv = qa_utils.inventory[i]
        if inv:
            for it in inv:
                if it not in candidate_items:
                    candidate_items.append(it)
    # Candidate actions: include only actions observed within the first ``n`` steps
    candidate_actions: List[str] = []
    seen_actions: set[str] = set()
    for i in range(n):
        act = qa_utils.actions[i]
        if act and act not in seen_actions:
            seen_actions.add(act)
            candidate_actions.append(act)

    # ------------------------------------------------------------------
    # Truncated helper functions for gains and location events
    # These helpers ensure that queries consider only steps within the first ``n``
    # steps of the trajectory when ``difficulty`` is positive.  If an event
    # occurs only after step ``n-1``, the functions return None.

    from collections import defaultdict as _defaultdict  # local alias to avoid clutter

    def first_gain_idx(item: str) -> Optional[int]:
        """Return the index (0-based) of the first time the specified item is gained within the first ``n`` steps.

        If the item is never gained within the truncated range, return None.
        """
        idxs = qa_utils.gain_events.get(item, [])
        for idx in idxs:
            if idx < n:
                return idx
        return None

    def nth_gain_idx(item: str, k: int) -> Optional[int]:
        """Return the index of the k‑th gain of the given item within the first ``n`` steps.

        If fewer than ``k`` gains occur before step ``n``, return None.
        """
        idxs = [idx for idx in qa_utils.gain_events.get(item, []) if idx < n]
        if len(idxs) >= k:
            return idxs[k - 1]
        return None

    def last_gain_idx(item: str) -> Optional[int]:
        """Return the index of the last gain of the given item within the first ``n`` steps.

        If the item is never gained within the truncated range, return None.
        """
        idxs = [idx for idx in qa_utils.gain_events.get(item, []) if idx < n]
        return idxs[-1] if idxs else None

    def first_enter_idx(loc: str) -> Optional[int]:
        """Return the first index where the player's location equals ``loc`` within the first ``n`` steps."""
        for i in range(n):
            if qa_utils.locations[i] == loc:
                return i
        return None

    def first_leave_idx(loc: str) -> Optional[int]:
        """Return the first index where the player leaves ``loc`` within the first ``n`` steps.

        A leave event is defined as the earliest step j (>0) such that
        ``locations[j-1] == loc`` and ``locations[j] != loc``.  If no such event
        occurs within the truncated range, return None.
        """
        for i in range(1, n):
            if qa_utils.locations[i - 1] == loc and qa_utils.locations[i] != loc:
                return i
        return None

    # Count how many items are gained per location within the first ``n`` steps.  This
    # dictionary is used for the F_location_most_item_gain tasks.  If a gain
    # occurs outside the truncated window, it is ignored.
    truncated_gain_locations: Dict[str, int] = _defaultdict(int)
    for itm, idxs in qa_utils.gain_events.items():
        for idx in idxs:
            if idx < n:
                loc = qa_utils.locations[idx]
                if loc:
                    truncated_gain_locations[loc] += 1

    # Keyword extraction
    location_tokens: set[str] = set()
    for loc in candidate_locations:
        if not loc:
            continue
        for part in re.findall(r"[a-zA-Z]{3,}", loc.lower()):
            location_tokens.add(part)

    # Build domain nouns from items, actions, and observation entities seen within the
    # truncation window.  Items and actions beyond the first ``n`` steps are ignored.
    items_vocab: set[str] = set()
    # items in inventory within range
    for i in range(n):
        inv = qa_utils.inventory[i]
        if inv:
            for it in inv:
                items_vocab.add(it.lower())
    # items gained within range
    for item, idxs in qa_utils.gain_events.items():
        for idx in idxs:
            if idx < n:
                items_vocab.add(str(item).lower())
                break
    # Objects from valid actions: take the second token when present, ignoring
    # directional commands and stop words
    objects_from_actions: set[str] = set()
    # Define synonyms to exclude direction-related nouns
    dir_synonyms = {
        "north","south","east","west","n","s","e","w",
        "northeast","ne","northwest","nw","southeast","se","southwest","sw",
        "up","u","down","d","in","out","inside","outside"
    }
    for i in range(n):
        acts = qa_utils.valid_actions[i]
        if acts:
            for a in acts:
                tokens = a.lower().split()
                if len(tokens) >= 2:
                    head = tokens[1]
                    if head not in dir_synonyms and len(head) >= 3:
                        objects_from_actions.add(head)
    # Observation nouns: capture words following articles (the/a/an) within range
    obs_nounish: set[str] = set()
    for i in range(n):
        obs = qa_utils.observations[i]
        if obs:
            for m in re.finditer(r"\b(?:the|a|an)\s+([a-z][a-z\-]{2,})\b", obs.lower()):
                obs_nounish.add(m.group(1))
    # Combine domain nouns and exclude location tokens
    domain_nouns = (items_vocab | objects_from_actions | obs_nounish) - location_tokens

    # Define a more comprehensive stopword list to filter out high-frequency
    # function words, verbs and boilerplate. 
    stopwords: set[str] = {
        "the", "and", "then", "open", "take", "go", "walk", "look", "enter",
        "for", "any", "just", "that", "might", "inside", "found", "opened",
        "information", "items", "item", "with", "without", "some", "into",
        "onto", "from", "there", "here", "make", "about", "been", "this", "what",
        "step", "steps", "reason", "observation", "keyword", "score", "location",
        "action", "player", "after", "before", "first", "second", "last", "gain",
        "leaves", "leaflet", "your", "you", "have", "has", "had", "having", "be",
        "am", "is", "are", "was", "were", "been", "being", "do", "does", "did",
        "doing", "can", "could", "may", "might", "must", "shall", "should", "will",
        "would", "again", "still", "just", "even", "over", "under", "around", "about",
        "onto", "around", "next", "continue", "need", "further", "area", "look",
        "examine", "try", "see"
    }
    # Minimum document frequency for a word to be considered (per reasoning/observation)
    MIN_DF_REASON = 3
    MIN_DF_OBS = 3

    # Candidate keywords from reasoning: gather word frequencies, prioritise domain nouns
    reason_df: Dict[str, int] = defaultdict(int)
    # Count keyword occurrences only within the first ``n`` reasoning sentences
    for i in range(n):
        reason = qa_utils.reasons[i]
        if not reason:
            continue
        words = set(re.findall(r"[a-z][a-z\-]{2,}", reason.lower()))
        for w in words:
            if w in stopwords or w in location_tokens:
                continue
            reason_df[w] += 1
    # Select domain nouns that appear in reasoning
    reason_domain = [w for w in domain_nouns if reason_df.get(w, 0) >= 1]
    # Select other high-frequency words exceeding DF threshold
    reason_common = [w for w, df in sorted(reason_df.items(), key=lambda x: -x[1]) if df >= MIN_DF_REASON]
    # Merge, preserving order and deduplicating
    candidate_reason_keywords: List[str] = []
    seen_kw: set[str] = set()
    for w in reason_domain + reason_common:
        if w not in seen_kw:
            candidate_reason_keywords.append(w)
            seen_kw.add(w)
    # Shuffle to diversify sampling and limit pool size (but large enough to sample later)
    random.shuffle(candidate_reason_keywords)
    candidate_reason_keywords = candidate_reason_keywords[: max(3 * max_per_type, 20)]

    # Candidate keywords from observations: similar extraction
    obs_df: Dict[str, int] = defaultdict(int)
    # Count keyword occurrences only within the first ``n`` observations
    for i in range(n):
        obs = qa_utils.observations[i]
        if not obs:
            continue
        words = set(re.findall(r"[a-z][a-z\-]{2,}", obs.lower()))
        for w in words:
            if w in stopwords or w in location_tokens:
                continue
            obs_df[w] += 1
    obs_domain = [w for w in domain_nouns if obs_df.get(w, 0) >= 1]
    obs_common = [w for w, df in sorted(obs_df.items(), key=lambda x: -x[1]) if df >= MIN_DF_OBS]
    candidate_obs_keywords: List[str] = []
    seen_obs: set[str] = set()
    for w in obs_domain + obs_common:
        if w not in seen_obs:
            candidate_obs_keywords.append(w)
            seen_obs.add(w)
    random.shuffle(candidate_obs_keywords)
    candidate_obs_keywords = candidate_obs_keywords[: max(3 * max_per_type, 20)]

    # For backward compatibility, retain 'candidate_keywords' as the reasoning keywords.
    candidate_keywords = candidate_reason_keywords.copy()

    # Helper to find occurrences of a keyword in reasons (exact word match)
    def reason_occurrences(keyword: str) -> List[int]:
        """Return indices of steps whose reasoning text contains the exact keyword.

        Matching is case‑insensitive and requires a word boundary on both sides.
        """
        kw = keyword.lower()
        positions: List[int] = []
        for i in range(n):
            reason = qa_utils.reasons[i]
            if not reason:
                continue
            if re.search(r"\b" + re.escape(kw) + r"\b", reason.lower()):
                positions.append(i)
        return positions

    # Helper to find occurrences of a keyword in observations (exact word match)
    def observation_occurrences(keyword: str) -> List[int]:
        """Return indices of steps whose observation contains the exact keyword.

        Matching is case‑insensitive and requires a word boundary on both sides.
        """
        kw = keyword.lower()
        positions: List[int] = []
        for i in range(n):
            obs = qa_utils.observations[i]
            if not obs:
                continue
            if re.search(r"\b" + re.escape(kw) + r"\b", obs.lower()):
                positions.append(i)
        return positions

    # Helper to compute visit counts and most visited locations in range [L,R]
    def visit_counts(L: int, R: int) -> Tuple[Dict[str, int], List[str]]:
        counts: Dict[str, int] = defaultdict(int)
        cur_loc = None
        for i in range(max(0, L), min(R, n - 1) + 1):
            loc = qa_utils.locations[i]
            if loc != cur_loc:
                if loc is not None:
                    counts[loc] += 1
                cur_loc = loc
        # Determine max visits
        if not counts:
            return counts, []
        max_visits = max(counts.values())
        most_visited = sorted([loc for loc, cnt in counts.items() if cnt == max_visits])
        return counts, most_visited

    # Helper to compute dwell counts and most dwell locations in range [L,R]
    def dwell_counts(L: int, R: int) -> Tuple[Dict[str, int], List[str]]:
        counts: Dict[str, int] = defaultdict(int)
        for i in range(max(0, L), min(R, n - 1) + 1):
            loc = qa_utils.locations[i]
            if loc:
                counts[loc] += 1
        if not counts:
            return counts, []
        max_dwell = max(counts.values())
        most_dwell = sorted([loc for loc, c in counts.items() if c == max_dwell])
        return counts, most_dwell

    # Deduplication helper
    def dedup_append(task_list: List[Dict[str, Any]], target: List[Dict[str, Any]], limit: int) -> None:
        seen: set = set()
        count = 0
        for t in task_list:
            key = (t["template"], t["question"])
            if key in seen:
                continue
            seen.add(key)
            target.append(t)
            count += 1
            if count >= limit:
                break

    # ----------------------------------------------------------------------
    # Single‑Hop Tasks
    # Template A_action: random steps asking for action
    tasks_A_action: List[Dict[str, Any]] = []
    step_indices = list(range(n))
    random.shuffle(step_indices)
    for idx in step_indices:
        act = qa_utils.actions[idx]
        q = f"At step {step_num(idx)}, what action did you execute?"
        a = act if act else "not answerable"
        tasks_A_action.append({"type": "Single‑Hop", "template": "A_action", "question": q, "paraphrase": para(q), "difficulty": 1, "range": [idx, idx], "gt": a})
    dedup_append(tasks_A_action, qas, max_per_type)

    # Template A_reason: random steps asking for first sentence of reasoning
    tasks_A_reason: List[Dict[str, Any]] = []
    random.shuffle(step_indices)
    for idx in step_indices:
        reason = qa_utils.reason_first_sentence[idx]
        q = f"At step {step_num(idx)}, what is the first sentence of your reasoning?"
        a = reason if reason else "not answerable"
        tasks_A_reason.append({"type": "Single‑Hop", "template": "A_reason", "question": q, "paraphrase": para(q), "difficulty": 1, "range": [idx, idx], "gt": a})
    dedup_append(tasks_A_reason, qas, max_per_type)

    # Template A_location: random steps asking for location
    tasks_A_location: List[Dict[str, Any]] = []
    random.shuffle(step_indices)
    for idx in step_indices:
        # Location before performing the action at this step
        loc = qa_utils.locations[idx]
        q = f"Before performing the action at step {step_num(idx)}, what is your location?"
        a = loc if loc else "not answerable"
        tasks_A_location.append({"type": "Single‑Hop", "template": "A_location", "question": q, "paraphrase": para(q), "difficulty": 1, "range": [idx, idx], "gt": a})
    dedup_append(tasks_A_location, qas, max_per_type)

    # Template A_reward: random steps asking for cumulative reward
    tasks_A_reward: List[Dict[str, Any]] = []
    random.shuffle(step_indices)
    for idx in step_indices:
        # Use cumulative reward at this step (reward after performing the action)
        rw = qa_utils.cum_rewards[idx] if idx < len(qa_utils.cum_rewards) else None
        q = f"After performing the action at step {step_num(idx)}, what is the cumulative reward so far?"
        a = str(rw) if rw is not None else "not answerable"
        tasks_A_reward.append({"type": "Single‑Hop", "template": "A_reward", "question": q, "paraphrase": para(q), "difficulty": 1, "range": [idx, idx], "gt": a})
    dedup_append(tasks_A_reward, qas, max_per_type)

    # Template A_keyword_occurrence: first/second/last/second last step whose reason contains keyword
    tasks_A_keyword: List[Dict[str, Any]] = []
    for kw in candidate_keywords:
        positions = reason_occurrences(kw)
        if not positions:
            # skip if keyword not found
            continue
        # first
        idx_first = positions[0]
        q = f"Which step is the first step whose reason mentions '{kw}'?"
        a = str(step_num(idx_first)) if idx_first is not None else "not answerable"

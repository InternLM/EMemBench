    # Function to add keyword‑based multi‑hop tasks for a given set of occurrences.
    def add_keyword_multi_hop_tasks(keyword: str, positions: List[int], context: str) -> None:
        """Append multi‑hop tasks for the given keyword and occurrence positions.
        """
        if not positions:
            return
        # For each occurrence type (first, second, last, second last)
        for occ_name in occurrence_names:
            pos_idx = get_occurrence_index(positions, occ_name)
            if pos_idx is None:
                continue

            max_delta_range = min(4, n - pos_idx)
            if max_delta_range <= 1:
                possible_deltas = [1]
            else:
                possible_deltas = list(range(1, max_delta_range))
            # Sample up to 3 deltas for diversity
            sampled_deltas = possible_deltas[:3]
            for delta in sampled_deltas:
                target = pos_idx + delta
                # Build question prefix depending on context
                if context == "reason":
                    prefix = f"After the {occ_name} step whose reason mentions '{keyword}',"
                else:
                    prefix = f"After the {occ_name} step whose observation mentions '{keyword}',"
                # Action: query the action executed delta steps later
                q_act = f"{prefix} what action is executed {delta} step(s) later?"
                if target < n and qa_utils.actions[target]:
                    ans_act = qa_utils.actions[target]
                    typ = "Multi‑Hop"
                else:
                    ans_act = "not answerable"
                    typ = "Adversarial"
                qas.append({"type": typ, "template": "B_keyword_after_action", "question": q_act, "paraphrase": para(q_act), "difficulty": delta + 1, "range": [pos_idx, min(n - 1, pos_idx + delta)], "gt": ans_act})
                # Location: ask for the location before performing the action at the target step
                q_loc = f"{prefix} what is the location before performing the action {delta} step(s) later?"
                if target < n and qa_utils.locations[target]:
                    ans_loc = qa_utils.locations[target]
                    typ2 = "Multi‑Hop"
                else:
                    ans_loc = "not answerable"
                    typ2 = "Adversarial"
                qas.append({"type": typ2, "template": "B_keyword_after_location", "question": q_loc, "paraphrase": para(q_loc), "difficulty": delta + 1, "range": [pos_idx, min(n - 1, pos_idx + delta)], "gt": ans_loc})
                # Reward: ask for the cumulative reward after performing the action at the target step
                q_sc = f"{prefix} what is the cumulative reward after performing the action {delta} step(s) later?"
                if target < n:
                    ans_sc = str(qa_utils.cum_rewards[target])
                    typ3 = "Multi‑Hop"
                else:
                    ans_sc = "not answerable"
                    typ3 = "Adversarial"
                qas.append({"type": typ3, "template": "B_keyword_after_reward", "question": q_sc, "paraphrase": para(q_sc), "difficulty": delta + 1, "range": [pos_idx, min(n - 1, pos_idx + delta)], "gt": ans_sc})
                # Observation: ask for the observation seen after performing the action at the target step
                q_obs = f"{prefix} what observation is seen after performing the action {delta} step(s) later?"
                if target < n and qa_utils.env_feedback[target]:
                    ans_obs = qa_utils.env_feedback[target].strip()
                    typ4 = "Multi‑Hop"
                else:
                    ans_obs = "not answerable"
                    typ4 = "Adversarial"
                qas.append({"type": typ4, "template": "B_keyword_after_observation", "question": q_obs, "paraphrase": para(q_obs), "difficulty": delta + 1, "range": [pos_idx, min(n - 1, pos_idx + delta)], "gt": ans_obs})
                # Ensure we do not exceed max_per_type per keyword: count tasks for action template containing the keyword
                count = sum(1 for t in qas if t.get("template") == "B_keyword_after_action" and keyword in t["question"])
                if count >= max_per_type:
                    break
            # Additional break if limit reached
            count = sum(1 for t in qas if t.get("template") == "B_keyword_after_action" and keyword in t["question"])

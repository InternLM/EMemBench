    # Multi‑Hop Tasks
    # B_keyword_after_* templates: for each keyword and occurrence type and delta we ask for action/location/score/observation
    occurrence_names = ["first", "second", "last", "second last"]
    # Map occurrence name to index function
    def get_occurrence_index(positions: List[int], name: str) -> Optional[int]:
        if not positions:
            return None
        if name == "first":
            return positions[0]
        if name == "second":
            return positions[1] if len(positions) >= 2 else None
        if name == "last":
            return positions[-1]
        if name == "second last":
            return positions[-2] if len(positions) >= 2 else None
        return None

